<?php
namespace foo;

class Arithmetic
{
    public function add($arg1, $arg2)
    {
        return $arg1 + $arg2;
    }

    public function subtract($arg1, $arg2)
    {
        return $arg1 - $arg2;
    }

    public function multiply($arg1, $arg2)
    {
        return $arg1 * $arg2;
    }

    public function divide($arg1, $arg2)
    {
        return $arg1 / $arg2;
    }
}
